import {
  isInvalid,
  isNull,
  isUndefined,
  refitCommonData,
  searchFromList,
} from 'easy-soft-utility';

import {
  unknownLabel,
  unlimitedWithStringFlag,
} from 'antd-management-fast-common';
import {
  buildDropdownMenu,
  buildFlexRadio,
  buildFlexSelect,
  buildFormRadio,
  buildFormSelect,
  buildOptionItem,
  buildRadioItem,
  buildSearchFormSelect,
  convertOptionOrRadioData,
} from 'antd-management-fast-component';
import { getMergeMetaData } from 'antd-management-fast-framework';

export function refitSubsidiaryStatusList({ withUnlimited = true }) {
  const { subsidiaryStatusList: list } = {
    subsidiaryStatusList: [],
    ...getMergeMetaData(),
  };

  if (withUnlimited) {
    return refitCommonData(list, unlimitedWithStringFlag);
  }

  return refitCommonData(list);
}

export function getSubsidiaryStatusName({ value, defaultValue = '' }) {
  if (isInvalid(value)) {
    return defaultValue;
  }

  const item = searchFromList(
    'flag',
    `${isNull(isUndefined(value) ? null : value) ? '' : value}`,
    refitSubsidiaryStatusList({ withUnlimited: false }),
  );

  return item == null ? '未知' : item.name;
}

export function renderSubsidiaryStatusOption({
  withUnlimited = true,
  adjustListDataCallback = null,
}) {
  const listData = refitSubsidiaryStatusList({ withUnlimited });

  return buildOptionItem({ list: listData, adjustListDataCallback });
}

export function renderSubsidiaryStatusRadio({
  withUnlimited = true,
  adjustListDataCallback = null,
}) {
  const listData = refitSubsidiaryStatusList({ withUnlimited });

  return buildRadioItem({ list: listData, adjustListDataCallback });
}

export function renderSearchSubsidiaryStatusSelect({
  withUnlimited = true,
  label = '状态',
  name = 'status',
  helper = null,
}) {
  const title = label || unknownLabel;

  return buildSearchFormSelect({
    label: title,
    name,
    helper,
    list: refitSubsidiaryStatusList({ withUnlimited }),
    dataConvert: convertOptionOrRadioData,
  });
}

